
import java.util.Scanner;

public class InferenciasVarPrimitivos {

    public static void main(String args[]) {
        var numeroEntero=10;
        System.out.println("El Valor de numero entero es: "+ numeroEntero);
        var numeroDouble=10.0;
        System.out.println("El Valor de numero double es: "+ numeroDouble);
        var numeroFloat = 10.0F;
        System.out.println("El Valor de numero float es: "+ numeroFloat);
                     
    }

}
